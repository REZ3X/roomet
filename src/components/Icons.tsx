export function DynamicIcon({
  name,
  className,
  size,
  style,
}: {
  name: string;
  className?: string;
  size?: number;
  style?: React.CSSProperties;
}) {
  const Icon = iconMap[name];
  if (!Icon) return <span className={className}>?</span>;
  return <Icon className={className} size={size} style={style} />;
}

